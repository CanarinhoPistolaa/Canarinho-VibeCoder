/**
 * Shared runtime utilities for scripted agent binaries (pi + hermes).
 *
 * This module exports the building blocks that both the pi and hermes
 * scripted runtimes use: prompt parsing, CLI spawning, behavior loading,
 * work-index tracking, placeholder substitution, journaling, and behavior
 * action application.
 *
 * Each runtime (scripted-agent-runtime.mjs for pi, scripted-hermes-runtime.mjs
 * for hermes) imports these functions and wires them together with its own
 * output emission (pi JSON events vs hermes plain text).
 */

import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

// ── Prompt parsing ──────────────────────────────────────────────────
//
// Both pi and hermes work prompts share the same structure generated by
// buildWorkPrompt(): a header line with "workflow X, agent Y, run Z" and
// a "path/to/canarinho" step claim command.

/**
 * Parse the work prompt to extract workflow/agent/run IDs and the CLI path.
 * Returns null if the prompt cannot be parsed.
 */
export function parsePrompt(input) {
  const header = input.match(
    /workflow "([^"]+)", agent "([^"]+)", run "([^"]+)"/,
  );
  if (!header) return null;
  const [, workflowId, agentId, runId] = header;

  const cliMatch = input.match(/(?:node )?"([^"]+)" step (?:claim|peek)/);
  if (!cliMatch) return null;
  const cliPath = cliMatch[1];

  const shortAgent = agentId.startsWith(`${workflowId}_`)
    ? agentId.slice(workflowId.length + 1)
    : agentId;

  return { workflowId, agentId, runId, cliPath, shortAgent };
}

// ── canarinho CLI invocation ──────────────────────────────────────────
//
// The prompt's CLI path may be the bin/canarinho shell launcher (exec it
// directly) or a .js entry point (run it through node). Detect by shebang
// so this works with either prompt/motor generation.

/**
 * Return a CLI spawn function bound to the given canarinho CLI path.
 * The returned function accepts (args, input?) and returns a spawnSync result.
 */
export function createCli(cliPath) {
  const isShellScript = (() => {
    try {
      const fd = fs.openSync(cliPath, "r");
      const buf = Buffer.alloc(2);
      fs.readSync(fd, buf, 0, 2, 0);
      fs.closeSync(fd);
      return buf.toString("utf-8") === "#!";
    } catch {
      return false;
    }
  })();

  const [command, baseArgs] = isShellScript
    ? [cliPath, []]
    : [process.execPath, [cliPath]];

  return (args, input) =>
    spawnSync(command, [...baseArgs, ...args], {
      encoding: "utf-8",
      cwd: process.cwd(),
      env: process.env,
      input,
      maxBuffer: 16 * 1024 * 1024,
    });
}

// ── Step CLI wrappers ───────────────────────────────────────────────

export function peekStep(cli, agentId, runId) {
  return cli(["step", "peek", agentId, "--run-id", runId]);
}

export function claimStep(cli, agentId, runId) {
  return cli(["step", "claim", agentId, "--run-id", runId]);
}

export function completeStep(cli, stepId, outputText) {
  return cli(["step", "complete", stepId], outputText);
}

export function failStep(cli, stepId, reason) {
  return cli(["step", "fail", stepId, reason]);
}

// ── Behaviors config ────────────────────────────────────────────────

const DEFAULT_CONFIG = { agents: {}, heartbeatTokens: 17, defaultTokens: 111 };

/**
 * Load behaviors config from a JSON file. Returns default config if the
 * file doesn't exist.
 */
export function loadBehaviors(behaviorsPath) {
  const config = { ...DEFAULT_CONFIG };
  if (behaviorsPath && fs.existsSync(behaviorsPath)) {
    Object.assign(config, JSON.parse(fs.readFileSync(behaviorsPath, "utf-8")));
  }
  return config;
}

/**
 * Look up the behavior for an agent at a given work index.
 * An array is consumed one entry per invocation, last entry repeating.
 * Tries shortAgent first, then falls back to full agentId.
 */
export function behaviorForInvocation(config, agentId, shortAgent, index) {
  const entry = config.agents[shortAgent] ?? config.agents[agentId];
  if (entry === undefined) return undefined;
  const list = Array.isArray(entry) ? entry : [entry];
  if (list.length === 0) return undefined;
  return list[Math.min(index, list.length - 1)];
}

/**
 * Increment and return the invocation count for a keyed agent.
 * The count is persisted in stateDir/<key>.workcount.
 */
export function nextWorkIndex(stateDir, key) {
  const countFile = path.join(stateDir, `${key}.workcount`);
  let count = 0;
  try {
    count = parseInt(fs.readFileSync(countFile, "utf-8"), 10) || 0;
  } catch {
    // first invocation
  }
  fs.mkdirSync(stateDir, { recursive: true });
  fs.writeFileSync(countFile, String(count + 1), "utf-8");
  return count;
}

// ── Placeholder substitution ────────────────────────────────────────
// {{cwd}} → workdir; {{input.KEY}} → "KEY: value" line from step input

/**
 * Parse KEY: value lines from a step input string.
 */
export function parseInputVars(input) {
  const vars = {};
  for (const line of input.split(/\r?\n/)) {
    const m = line.match(/^([A-Z][A-Z0-9_]*):\s*(.*)$/);
    if (m) vars[m[1]] = m[2].trim();
  }
  return vars;
}

/**
 * Substitute {{cwd}} and {{input.KEY}} placeholders in text.
 * Throws if a referenced KEY is missing from inputVars.
 */
export function substitute(text, cwd, inputVars) {
  return text
    .replaceAll("{{cwd}}", cwd)
    .replace(/\{\{input\.([A-Za-z0-9_]+)\}\}/g, (_, key) => {
      const upper = key.toUpperCase();
      if (!(upper in inputVars)) {
        throw new Error(
          `no "${upper}:" line in step input for placeholder {{input.${key}}}`,
        );
      }
      return inputVars[upper];
    });
}

// ── Journaling ──────────────────────────────────────────────────────

/**
 * Append a JSON line to stateDir/invocations.jsonl.
 * Never throws — silently drops on any failure.
 */
export function logInvocation(stateDir, entry) {
  if (!stateDir) return;
  try {
    fs.mkdirSync(stateDir, { recursive: true });
    fs.appendFileSync(
      path.join(stateDir, "invocations.jsonl"),
      JSON.stringify({
        ts: new Date().toISOString(),
        pid: process.pid,
        ...entry,
      }) + "\n",
      "utf-8",
    );
  } catch {
    // never let diagnostics kill the round
  }
}

/**
 * Log an error and exit. label distinguishes the runtime (e.g. "scripted-agent"
 * vs "scripted-hermes") in stderr messages.
 */
export function fatal(stateDir, label, note, exitCode = 1) {
  logInvocation(stateDir, { phase: "error", note });
  process.stderr.write(`${label}: ${note}\n`);
  process.exit(exitCode);
}

// ── Behavior action application ─────────────────────────────────────

/**
 * Apply scripted behavior actions (edits, writes, commands) in the given
 * working directory. Throws on any failure — callers should catch and
 * turn the error into a step fail report.
 */
export function applyBehaviorActions(behavior, cwd, inputVars) {
  for (const edit of behavior.edits ?? []) {
    const filePath = path.resolve(cwd, substitute(edit.file, cwd, inputVars));
    const content = fs.readFileSync(filePath, "utf-8");
    const find = substitute(edit.find, cwd, inputVars);
    if (!content.includes(find)) {
      throw new Error(
        `scripted edit: pattern not found in ${edit.file}: ${find}`,
      );
    }
    fs.writeFileSync(
      filePath,
      content.replaceAll(find, substitute(edit.replace, cwd, inputVars)),
      "utf-8",
    );
  }
  for (const write of behavior.writes ?? []) {
    const filePath = path.resolve(cwd, substitute(write.file, cwd, inputVars));
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(
      filePath,
      substitute(write.content, cwd, inputVars),
      "utf-8",
    );
  }
  for (const command of behavior.commands ?? []) {
    const rendered = substitute(command, cwd, inputVars);
    const r = spawnSync("bash", ["-c", rendered], {
      cwd,
      encoding: "utf-8",
      env: process.env,
      maxBuffer: 16 * 1024 * 1024,
    });
    if (r.status !== 0) {
      throw new Error(
        `scripted command failed (exit ${r.status}): ${rendered}\n${r.stderr}`,
      );
    }
  }
}
